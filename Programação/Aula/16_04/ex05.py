#laço contato com while
#contator
contator=0 #variavel de controle
while contator <5:
    n1=float(input('Digite a primeira nota: '))
    n2=float(input('Digite a segunda nota: '))
    media=(n1+n2)/2
    print('Média = ', media)
    contator = contator+1 #atualização da variável de controle